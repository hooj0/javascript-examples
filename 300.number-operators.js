/***************************************************************************
 * 对象类型数字运算
 *   - toString
 *   - valueOf
 ***************************************************************************/

let price = {
    valueOf() {
        return 5;
    },
    toString() {
        return 'price';
    }
};

// --------------------------------------------------------------------
// 优先使用 valueOf 进行运算
// --------------------------------------------------------------------
console.log(price + 10);    // 15
console.log(price - 10);    // -5

let amount = {
    toString() {
        return 20;
    }
};

// --------------------------------------------------------------------
// 没有valueOf时，toString 也可以进行运算
// --------------------------------------------------------------------
console.log(amount + 10);
console.log(amount - 10);